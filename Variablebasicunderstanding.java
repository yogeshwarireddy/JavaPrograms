package variables;

public class Variablebasicunderstanding {

	public static void main(String[] args) {
		// Variable declaration
		int a; // Here we are declaring a variable 'a' of type int
	    int b;
	    int c;
	    // Variable initialization
	    a=10; // Here we are initializing the variable 'a' with the value 10
	    b=20; // Here we are initializing the variable 'b' with the value 20
	    c=30; // Here we are initializing the variable 'c' with the value 30
	
	  System.out.println("Value of a is " + a); // Using the variable 'a' to print its value);
	  System.out.println("Value of b is " + b); // Using the variable 'b' to print its value);
	  System.out.println("Value of c is " + c); // Using the variable 'c' to print its value);
	  // Note: Variable declaration and initialization can be done in one line as well
	  // Example: int d = 40; // This declares and initializes variable 'd' in one line
	  // Example of declaring and initializing multiple variables in one line
	  	int d = 40, e = 50, f = 60; // Here we are declaring and initializing multiple variables in one line
	  	System.out.println("Value of d is " + d); // Using the variable 'd' to print its value);
	}
}